#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	l;
	size_t	i;
	char	*s2;

	l = 0;
	while (s1[l])
		l++;
	s2 = (char *)malloc (l + 1);
	if (!s2)
		return (NULL);
	i = 0;
	while (i <= l)
	{
		s2[i] = s1[i];
		i++;
	}
	return (s2);
}
